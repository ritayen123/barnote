// Base spirit color coding
export function getBaseColor(baseSpirit: string): string {
  const colors: Record<string, string> = {
    Whiskey: "#c97a2c",
    Gin: "#6ba3be",
    Vodka: "#a8b5c4",
    Rum: "#8b5e3c",
    Tequila: "#7cb342",
    Brandy: "#a0522d",
    Multiple: "#9c6ab8",
    Champagne: "#e8d44d",
    Wine: "#8b2252",
    Liqueur: "#d4668e",
    Beer: "#daa520",
    Sake: "#e8e8e8",
    Shochu: "#c0c0c0",
    Baijiu: "#f5f5dc",
  };
  return colors[baseSpirit] || "#d4a053";
}
